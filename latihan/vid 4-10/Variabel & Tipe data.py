# Variabel adalah sebuah wadah atau kotak untuk menyimpan nilai atau value
print("==== Mengenal Variabel ====")
x = 10  # x adalah variabel nya, = adalah operator penugasan, 10 adalah nilai yang disimpan dalam x
print(x,"\n")



# selanjutnya dalam variabel kita bisa simpan berbagai tipe data
print("==== Tipe Data ====")
# 1. Tipe data integer (bilangan bulat)
data_integer = 10
print(data_integer, "Adalah tipe data integer", type(data_integer))

# 2. Tipe data float (angka dengan koma)
data_float = 5.5
print(data_float, "Adalah tipe data float", type(data_float))

# 3. Tipe data String (kumpulan karakter)
data_string = "Halo saya rio sedang belajar python"
print("'",data_string,"'", "Adalah tipe data string", type(data_string))

# 4. Tipe data boolean (hanya ada true dan false)
data_boolean = False
print(data_boolean, "Adalah tipe data boolean", type(data_boolean), "\n")
